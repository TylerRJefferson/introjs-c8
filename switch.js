//Example of Switch statements

const animal = 'Snake'

switch (animal) {
  case 'Dog':
    console.log('woof woof')
    break
  case 'Cat':
    console.log('meow meow')
    break
  case 'Bird':
    console.log('tweet tweet')
    break
  case 'Snake':
    console.log('hiss hiss')
    break
  default:
    console.log('I do not know that animals sound.')
    break
}
